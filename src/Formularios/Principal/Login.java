    public void AccionLogin(){
        if(new libLogin().ComprobarLogin()){
            JOptionPane.showMessageDialog(null, "BIENVENIDO " + txtusername.getText().toUpperCase(),"WELCOME!",JOptionPane.INFORMATION_MESSAGE);
            new Principal().setVisible(true);
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null,"USUARIO O CONTRASEÃ‘A INCORRECTA","ACCESS DENIED",JOptionPane.ERROR_MESSAGE);
            this.contador++;
            if((contador % 3) == 0 && contador != 0){
                new libLogin().LoginTimer();
                JOptionPane.showMessageDialog(null,"HA EXCEDIDO EL MAXIMO DE INTENTOS PERMITIDOS, POR FAVOR ESPERE 30 SEGUNDOS","ACCESS DENIED",JOptionPane.ERROR_MESSAGE);
            }
            txtpassword.setText("");
            
        }
    }
    

